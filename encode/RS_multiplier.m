function [mult_result] = RS_multiplier(a,b)
    q=zeros(15,1);
    q(1)=a(1)*b(1);
    q(2)=a(2)*b(1) + a(1)*b(2);
    q(3)=a(3)*b(1) + a(2)*b(2) + a(1)*b(3);
    q(4)=a(4)*b(1) + a(3)*b(2) + a(2)*b(3) + a(1)*b(4);
    q(5)=a(5)*b(1) + a(4)*b(2) + a(3)*b(3) + a(2)*b(4) + a(1)*b(5);
    q(6)=a(6)*b(1) + a(5)*b(2) + a(4)*b(3) + a(3)*b(4) + a(2)*b(5) + a(1)*b(6);
    q(7)=a(7)*b(1) + a(6)*b(2) + a(5)*b(3) + a(4)*b(4) + a(3)*b(5) + a(2)*b(6) + a(1)*b(7);
    q(8)=a(8)*b(1) + a(7)*b(2) + a(6)*b(3) + a(5)*b(4) + a(4)*b(5) + a(3)*b(6) + a(2)*b(7) + a(1)*b(8);
    q(9)=a(8)*b(2) + a(7)*b(3) + a(6)*b(4) + a(5)*b(5) + a(4)*b(6) + a(3)*b(7) + a(2)*b(8);
    q(10)=a(8)*b(3) + a(7)*b(4) + a(6)*b(5) + a(5)*b(6) + a(4)*b(7) + a(3)*b(8);   
    q(11)=a(8)*b(4) + a(7)*b(5) + a(6)*b(6) + a(5)*b(7) + a(4)*b(8); 
    q(12)=a(8)*b(5) + a(7)*b(6) + a(6)*b(7) + a(5)*b(8); 
    q(13)=a(8)*b(6) + a(7)*b(7) + a(6)*b(8); 
    q(14)=a(8)*b(7) + a(7)*b(8); 
    q(15)=a(8)*b(8); 
    q=mod(q,2);
    mult_result=zeros(8,1);
    mult_result(8)=q(14)+q(13)+q(12)+q(8);
    mult_result(7)=q(13)+q(12)+q(11)+q(7);
    mult_result(6)=q(12)+q(11)+q(10)+q(6);
    mult_result(5)=q(15)+q(11)+q(10)+q(9)+q(5);
    mult_result(4)=q(13)+q(12)+q(10)+q(9)+q(4);
    mult_result(3)=q(14)+q(13)+q(11)+q(9)+q(3);
    mult_result(2)=q(15)+q(14)+q(10)+q(2);
    mult_result(1)=q(15)+q(14)+q(13)+q(9)+q(1);
    mult_result = mod(mult_result,2);
end